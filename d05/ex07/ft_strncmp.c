int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int a;
	int b;
	int i;

	a = 0;
	b = 0;
	i = n;
	while (*s1 && n > 0)
	{
		a += *s1;
		s1++;
		n--;
	}
	n = i;
	while (*s2 && n > 0)
	{
		b += *s2;
		s2++;
		n--;
	}
	return (a - b);
}
