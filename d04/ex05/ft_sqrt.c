int		ft_sqrt(int nb)
{
	int sqrt;

	sqrt = 1;
	if (nb == 2147483647)
		return (-1);
	while ((sqrt * sqrt) < nb)
		sqrt++;
	if ((sqrt * sqrt) == nb)
		return (sqrt);
	return (0);
}
